var PVector = function() {
function PVector(x, y, z) {
this.x = x || 0;
this.y = y || 0;
this.z = z || 0;
}
PVector.dist = function(v1, v2) {
return v1.dist(v2)
};
PVector.dot = function(v1, v2) {
return v1.dot(v2)
};
PVector.cross = function(v1, v2) {
return v1.cross(v2)
};
PVector.subtract = function(v1, v2) {
return v1.sub(v2)
};

PVector.angleBetween = function(v1, v2) {
return Math.acos(v1.dot(v2) / (v1.mag() * v2.mag()))
};
PVector.prototype = {
set: function(v, y, z) {
if (arguments.length === 1) this.set(v.x || v[0] || 0, v.y || v[1] || 0, v.z || v[2] || 0);
else {
this.x = v;
this.y = y;
this.z = z
}
},
get: function() {
return new PVector(this.x, this.y, this.z)
},
mag: function() {
var x = this.x,
y = this.y,
z = this.z;
return Math.sqrt(x * x + y * y + z * z)
},
add: function(v, y, z) {
if (arguments.length === 1) {
this.x += v.x;
this.y += v.y;
this.z += v.z
} else {
this.x += v;
this.y += y;
this.z += z
}
},
sub: function(v, y, z) {
if (arguments.length === 1) {
this.x -= v.x;
this.y -= v.y;
this.z -= v.z
} else {
this.x -= v;
this.y -= y;
this.z -= z
}
},
mult: function(v) {
if (typeof v === "number") {
this.x *= v;
this.y *= v;
this.z *= v
} else {
this.x *= v.x;
this.y *= v.y;
this.z *= v.z
}
},
div: function(v) {
if (typeof v === "number") {
this.x /= v;
this.y /= v;
this.z /= v
} else {
this.x /= v.x;
this.y /= v.y;
this.z /= v.z
}
},
dist: function(v) {
var dx = this.x - v.x,
dy = this.y - v.y,
dz = this.z - v.z;
return Math.sqrt(dx * dx + dy * dy + dz * dz)
},
dot: function(v, y, z) {
if (arguments.length === 1) return this.x * v.x + this.y * v.y + this.z * v.z;
return this.x * v + this.y * y + this.z * z
},
cross: function(v) {
var x = this.x,
y = this.y,
z = this.z;
return new PVector(y * v.z - v.y * z, z * v.x - v.z * x, x * v.y - v.x * y)
},
normalize: function() {
var m = this.mag();
if (m > 0) this.div(m)
},
limit: function(high) {
if (this.mag() > high) {
this.normalize();
this.mult(high)
}
},
heading2D: function() {
return -Math.atan2(-this.y, this.x)
},
toString: function() {
return "[" + this.x + ", " + this.y + ", " + this.z + "]"
},
array: function() {
return [this.x, this.y, this.z]
}
};

function createPVectorMethod(method) {
return function(v1, v2) {
var v = v1.get();
v[method](v2);
return v
}
}
for (var method in PVector.prototype)
if (PVector.prototype.hasOwnProperty(method) && !PVector.hasOwnProperty(method)) PVector[method] = createPVectorMethod(method);
return PVector
}();